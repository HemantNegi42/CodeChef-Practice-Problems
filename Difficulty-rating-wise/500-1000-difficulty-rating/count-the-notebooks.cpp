/* You know that 1 kg of pulp can be used to make 1000 pages and 1 notebook consists of 100 pages.
Suppose a notebook factory receives N kg of pulp, how many notebooks can be made from that? */

#include <bits/stdc++.h>
using namespace std;

int main() {
    int T;
    cin >> T;
    while (T--) {
        int N;
        cin >> N;
        cout << N * 10 << endl;
    }
    return 0;
}
